function ChunkCounter(maxChunks)
{
    "use strict";

    this.limit = maxChunks;
    this.index = 0;

    //==================================================================================================================
    // Increases the counter by one.
    //==================================================================================================================
    this.increaseCounter = function()
    {
        this.index++;
    };

    //==================================================================================================================
    // Reaches the counter the limit?
    //==================================================================================================================
    this.finish = function()
    {
        if(this.index < this.limit)
        {
            return false;
        }
        else
        {
            return true;
        }
    };

    //==================================================================================================================
    // Resets the counter.
    //==================================================================================================================
    this.resetCounter = function(maxChunks)
    {
        this.index = 0;
        this.limit = maxChunks;
    };

    //==================================================================================================================
    // Restarts the counter with the given limit value.
    //==================================================================================================================
    this.restart = function()
    {
        this.index = 0;
    };
}